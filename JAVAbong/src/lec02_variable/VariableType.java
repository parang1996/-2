package lec02_variable;

public class VariableType {
	
	// 변수의 타입
	// primary Type 기본타입
	// 정수타입
	
	byte byteVar = 1;
	short shortVar = 2;
	int intVal = 3;
	long longVal = 4;
	char charVar = 5;
	char charVal = 'a';
	
	// 실수타입
	
	float floatVar = 3.14f;
	float floatVal = 3.14F;
	double doubleVar = 3.14;
	
	// 불리언타입 (true/false)
	
	boolean boolVar = true;
	boolean boolVal = false;
	
	//
	
	String strVar = "abc";
}
